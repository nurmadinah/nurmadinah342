public class Percabangan_switch {
    public static void main(String[] args) {
        int pilihan = 200;
    switch (pilihan) {
        case 100:
            System.out.println("Kamu Memilih Pilihan pertama Yaitu 100");
            break;
        case 200:
            System.out.println("Kamu Memilih Pilihan Kedua Yaitu 200");
            break;
        default:
            System.out.println("Tidak Ada yang terpilih");
        }
    }
}
